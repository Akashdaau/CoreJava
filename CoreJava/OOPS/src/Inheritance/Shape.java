package Inheritance;

public class Shape {
	public String color;
	public int borederwidth;
	public double area;
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getBorederwidth() {
		return borederwidth;
	}
	public void setBorederwidth(int borederwidth) {
		this.borederwidth = borederwidth;
	}
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	
	

}
