package getset;

public class Person {
	public String name;
	public String address;
	public int age;
	
	public String getNamee()
	{
		return name;
	}
	public void setNamee(String name)
	{
		this.name= name;
	}
	public String getAddress()
	{
		return address;
	}
	public void setAddress(String address)
	{
		this.address = address;
	}
	public int getAge()
	{
		return age;
	}
	public void setAge(int age)
	{
		this.age= age;
	}

}

